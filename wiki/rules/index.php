            <h2 class="blog-post-title">DarCraft Rules</h2>
              <hr>
            <p>This page serves as a general overview of the rules of DarCraft. The rules are subject to change without notice and all rules may not be present - as long as you use common sense and always aim to be a pleasant player, you shouldn't run into any issues.</p>
              <br>
            <h3>Griefing is not allowed.</h3>
              <p>This is a pretty straightforward rule. For anyone not familiar with the term, griefing is destroying or modifying creations that are not your own without permission from the owner. Griefing is not allowed in any form, and will be punished based on the severity of the grief.</p>
              <br>
            <h3>Be Respectful</h3>
            <p>Again, a very simple rule. Treat everyone with respect - this also means no racist, sexist or hurtful comments. Part of this rule also includes no cursing. This is due to the fact that we aim to be a server that players of all ages can come on and play, and cursing can cause issues with that. Cursing is allowed in private message if both parties consent, but please continue to be respectful.</p>
              <br>
            <h3>No Hacks or Mods</h3>
            <p>While we expect that a majority of our users do not use any hacks or mods, this rule is in place to prevent players from gaining an unfair advantage over others. This means no "hacked clients" or mods that give an unfair advantage such as Xray Mods, Chest Finders, or anything of the sorts. The only mods that we permit on the server are mods that do not give you an unfair advantage over other players. The most common ones that we allow are OptiFine, Inventory Organizer Mods, and Minimap mods. If you are going to use a minimap mod, please ensure that it isn't doing things like revealing things like chests and mobs. If you are unsure if a mod is allowed or not, simply message a staff member and they will be sure to help you.</p>
              <br>
            <h3>No Caps, Flooding, Spamming, or Advertising</h3>
            <p>In order to help players have an enjoyable time on the server, these rules are implemented. We tend to be very strict about this rule, due to the fact that it can be bothersome to others to have their chat filled with players sending messages with all caps that could be misinterpreted, tons of messages by the same player and repeated messages. When it comes to advertising, we are extremely strict. This means that advertising another server in any way, shape, or form is not allowed by any means on the server. While the use of caps, flooding, or spamming will ususally be punished with a simple warning, advertising usually results in a minimum 2 week ban.</p>
              <br>
            <h3>Use Common Sense</h3>
            <p>While the rules above don't cover everything, we feel that this rule is the most important. If this rule is followed, you should run into no problems on DarCraft. This server was created for players to come on and enjoy Minecraft together, and as long as you continue to use proper judgement when making decisions on the server, you should never run into any issues. If you are unsure if something you are about to do is against the rules, simply ask yourself, "Would the staff members be okay with this?". If you answered no, then it's probably not a good idea. Remember that you can always ask a staff member if something is allowed or not.</p>
              <br>
            <h3>Have Fun!</h3>
            <p>You aren't required to have fun, but we sure hope you do. If you are ever unhappy with something on the server and feel that something can be done about it, the <a href="../../forums">forums</a> are always open to post your feedback. We are always aiming to improve the server and continue to provide a fun environment for players to enjoy themselves on, and your feedback is extremely valuable to us.</p>

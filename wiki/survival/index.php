            <h2 class="blog-post-title">The Survival World</h2>
              <hr>
              <p>The survival world is the main attraction on DarCraft. In this world you are able to explore the world, mine, farm, and build anything you wish with the materials you obtain. To access the Survival World, type <code><font color=00AAAA>/survival</font></code>. This command will open a menu offering areas to teleport to in the world.</p>
                    
              <center><img src="warpfar.png" class="img-thumbnail"></center><br>
            <h3>Finding a Starting Place</h3>
            <p>If you're unable to find a place directly from a warp far region, you can use DarCraft's <a href="http://darcraft.net:8123">Dynmap</a> to help find a place to live. It's important to note that all areas shaded in red or blue are <i>claimed areas</i>, which means you are unable to build in that area. Typically players want to find a place to start playing away from everyone else, but you can live wherever you'd like. Once you've found a spot, mouse over the area and locate the coordinates on the top left of the window. This is where the spot is located in the world, and if you press <code><font color=00AAAA>F3</font></code>, or if you're on Mac, <code><font color=00AAAA>Fn + F3</font></code> in-game, you will be able to find your coordinates.</p>
              <center><img src="dynmap.png" class="img-thumbnail"></center>
              <br>
            <h3>Claiming Your Land</h3>
              <p>Once you've found a place to start building, you're going to want to claim your land. Claiming your land protects everything inside the claim from those not trusted to it. To claim your land for the first time, simply place a Chest and a claim should be automatically created for you. If not, or if you'd like to create your claim manually, you can use  a Golden Shovel to <i>right click</i> opposite ends of the area you would like to claim. If done correctly, it should notify you as well as show an outline of your claim with Gold Blocks and Glowstone.</p>
              <center><img src="claimoutline.png" class="img-thumbnail"></center><br>
              <p>You can obtain a Golden Shovel with <code><font color=00AAAA>/kit protect</font></code>. This kit also includes a stick, which can be used to right click an area to see if it is claimed as well as reveal the outline of the claim if there is one. For more information regarding claiming, please refer to <a href="https://www.youtube.com/watch?v=VDsjXB-BaE0">this</a> video.</p>


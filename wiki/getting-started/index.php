            <h2 class="blog-post-title">Getting Started</h2>
              <hr>
            <h2>Joining the Server</h2>
              <p>In order to get started on the server, you must first, obviously, join the server. To do so, launch your Minecraft PC client and go to Multiplayer. From there you will need to click "Add Server" and type in <code><font color=00AAAA>darcraft.net</font></code> for the Server Address. After saving those details, connect to the server by double clicking it on your server page.</p>
                    <center><img src="connect.png" class="img-thumbnail"></center> <br>
            <h3>Navigating Spawn</h3>
            <p>Afecter connecting to the server, you will <i>hopefully</i> be greeted by the players of DarCraft and placed into the spawn world. Here is where everything is located - the rules, the event arena, the markets and portals to the other worlds. Take some time to navigate the spawn to familiarize yourself with everything. The spawn is important, as it serves as a hub for all your needs on the server.</p>
            <h3>Playing Survival</h3>
              <p>If you're looking to play survival, it's not too difficult to get started - especially considering the tools provided to you upon joining the server. The easiest way to jump into the world is by typing <code><font color=00AAAA>/survival</font></code> in chat which will open a menu showing you all available spawn points in the world.</p>
                    <center><img src="survival.png" class="img-thumbnail"></center> <br>
              <p>By hovering your mouse over a compass, you will be shown the biomes located nearby, the coordinates of the point, as well as a warning if the location is too crowded. Click on one of the compasses in order to be teleported to the location. If you do not find a location that you like, you may type in chat asking a staff member if they can help you find a place to get started.</p>
              <p>Once you find a place to start a home, it's important to claim your land so that you are unable to be griefed. For more information on that, along with any other survival resources, visit the <a href="../survival-world">Survival World</a> wiki page.</p>
              <h3>Playing Creative</h3>
                <p>If you're new to the server and looking to play creative, your only option is the Plot World. The Plot World is a flat creative world with plots divided up for players to build on. To go to the Plot World, type <code><font color=00AAAA>/plotworld</font></code>. From there, typing <code><font color=00AAAA>/p auto</font></code> will claim a plot for you to build on. If you ever get lost in the Plot World, use <code><font color=00AAAA>/p home</font></code> to be teleported back to your plot. For more information on the Plot World and how to use the plugin, visit the <a href="../plot-world">Plot World</a> wiki page.</p>
                <p>If you've played DarCraft long enough to be ranked up to <a href="../ranks">Trusted</a>, you will have access to the Freebuild World. This is a normal creative world without claims, allowing you to create much more elaborate builds with a greater area and the environment to build upon. To visit the Freebuild world, type <code><font color=00AAAA>/freebuild</font></code>. For more information on the Freebuild World, visit the <a href="../freebuild-world">Freebuild World</a> wiki page.</p>
              
              <br>
              
              <p>Of course, there's more to DarCraft than this page. With the economy, ranks, plugins and more, it's impossible to cover it all on one page. To learn more about anything you are interested in, click a link on the sidebar. If it's not there, you can simply ask a staff member online with <code><font color=00AAAA>/helpop</font></code>. Good luck on the server, Darcraftian! </p>


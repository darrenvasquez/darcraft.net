            <p>Welcome to DarCraft - A Survival and Creative Minecraft Server created by <a href="../../members/1">Darren</a> on November 3rd, 2011.</p>
            <h2>The History of DarCraft</h2>
              <p>DarCraft, originally dubbed "Darren's Minecraft Server", was created with the intent of bringing all of Darren's friends together to enjoy Minecraft together doing whatever they'd like. All of the beginning players were all friends from another server looking for a new home. They all worked together to create a friendly community.</p>
              <p>After a month of being a private, invite-only server, Darren's Minecraft Server became a public server, with staff, plugins, rules, and all the necessary tools to succeed. Without the help of Darren's friends, most notably SirBumpington, who helped hugely in the setup of the server and its' plugins, the server wouldn't have evolved into what it is today. </p>
            <h3>The Establishment of DarCraft and DarCraft PvP</h3>
            <p>After months of miraculous and unprecidented growth of a simple server hosted on a teenager's home PC, it was time for expansion. The opening of donations for players willing to help contribute to the server coupled with the unfortnate crash of the server PC, meant that it was time for the server to be hosted by a company rather than on a PC.</p>
            <p>After migrating the server to something more secure in April of 2012, the name "DarCraft" was established in place of "Darren's Minecraft Server". The consistent growth of the server also paved the way for a new server that Darren decided to open - DarCraft PvP. This server was opened with the goal of providing DarCraft's players with a place to do what they weren't able to do on the main server - use bad language, grief, raid, and of course, PvP. Surprisingly, DarCraft PvP gained more popularity than the main server - while both continued to grow and develop their own communitites, DarCraft PvP attracted large groups of people looking to establish factions and become to best on the server. The server flourished for around 8 months, but after people got bored of the server it unfortunately resulted in the end of its' run.</p>
            <h3>The Future of DarCraft</h3>
            <p>Throughout the years, DarCraft has had tons of success. What started out as a simple server for friends evolved into a server that has lasted longer than anyone could've ever imagined. DarCraft continues to evolve each year, trying new things and continuing to try to bring players a positive atmosphere to come on and enjoy the game of Minecraft with friends. Even though everything hasn't been successful for the server, DarCraft continues to provide a high-quality server that values its' players more than anything, and the staff of DarCraft are committed to providing the players with a place to play for years to come.</p>

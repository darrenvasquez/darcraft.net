            <h2 class="blog-post-title">Ranks</h2>
              <hr>
              
            <p>DarCraft, like most servers, has different ranks for players. This page will give a basic overview of the ranks and what they mean.</p>
            <h2>Non-Staff Ranks</h2>
              <p>All of these Non-Staff Ranks are obtainable, whether it be through playing on the server or donating.</p>
              
            <h3><font color=2ECC71>Member</font></h3>
            <p>Upon first joining the server, you are granted with the Member rank. This rank gives you all of the basic commands - chatting, joining the Survival and Plot World, and anything else necessary to succeed on the server.</p>
              
            <h3><font color=1E8449>Trusted</font></h3>
            <p>After being present on the server for a total time of <i>12 Hours</i>, you will be granted the Trusted rank. This rank is extremely similar to Member, with the addition of you being able to build in the Freebuild World. This rank establishes the fact that you have played on the server for long enough to be able to be granted the priviledge to join a creative world without claims.</p>
              
            <h3><font color=5DADE2>Devoted</font></h3>
            <p>After being present on the server for a total time of <i>3 Days</i>, you will be granted the Devoted rank. Currently, other than the prefix, this rank offers nothing of significant value. We are currently looking into granting permissions for this rank.</p>
              
            <h3><font color=8E44AD>Veteran</font></h3>
            <p>After being present on the server for a total time of <i>14 Days</i>, you will be granted the Veteran rank. This rank shows that you have spent a lot of time on the server and deserve outstanding recognition - making this the hardest rank to achieve and least populated rank other than staff ranks. Players with this rank are granted permission to fly in the Survival Worlds, along with the ability to set a nickname.</p>
              
            <h3><font color=2ECC71>Supporter</font></h3>
            <p>The Supporter rank is a paid rank that grants all the permissions of Veteran, and more. Access to special tools and armor, a special donator chat channel, flying, and a Virtual Chest that persists through most server resets are some of the most valuable things that players gain by donating for this rank.</p>
              
            <h3><font color=D68910>Darcraftian</font></h3>
            <p>The Darcraftian rank is also a paid rank, except a tier higher than Supporter. This grants all the permissions of the Supporter rank, along with things like disabling your teleport, the ability to keep your player fed, WorldEdit in the creative worlds, and more.</p>
              
             <br>
              
            <h2>Staff Ranks</h2>
              <p>Becoming a staff member on DarCraft is extremely difficult to do - the requirements are strict and very few people are selected every year for the positions needed. The only staff ranks that players are capable of achieving are Trial Moderator, Moderator and Administrator. </p>
              
              
            <h3><font color=D68910>Trial Moderator</font></h3>
            <p>A Trial Moderator, or TrialMod for short, is a player who has been granted the opportunity to become staff. This rank grants all the permission of a Moderator, except establishes that the player is in training to become an official Staff Member. This player is looking to prove himself to the staff in order to become part of the staff team.</p>
              
            <h3><font color=5DADE2>Moderator</font></h3>
            <p>A Moderator is the lowest non-temporary Staff Rank, which grants the player with the permissions needed to help a player when they are in need. They are capable of things like checking for griefs, responding to reports, and more.</p>
              
            <h3><font color=AA0000>Admin</font></h3>
            <p>An Admin is a rank above a Moderator, and typically the highest rank that a player can achieve. This rank is typically achieved after a player has went above and beyond as Moderator, showing that they are capable of becoming an Admin. They are capable of everything a Moderator is, coupled with rolling back griefs and a couple other commands to ensure that all rules are followed by the community.</p>
              
            <h3><font color=8E44AD>Founder</font></h3>
            <p>A Founder is a special rank granted to players that have contributed <i>significantly</i> to DarCraft in the past. Whether it be through years of devotion as a staff member or assistance on getting DarCraft started as a server, these players have proven to be vital to the success of DarCraft throughout the years.</p>
              
            <h3><font color=AA0000>Developer</font></h3>
            <p>The Developer, also known as x128, takes sole control of this rank. He develops custom plugins for the server and helps ensure that everything runs smoothly.</p>            
              
            <h3><font color=D68910>Owner</font></h3>
            <p>The Owner of the server. This rank is reserved exclusively for the owner, DaRr3n911.</p>
              
